package com.example.pizza_configurator;

public class Pizza {

    double pizza_size = 0;
    double pizza_pepperoni = 0;
    double pizza_cheese = 0;
    double pizza_pineapple = 0;

    public Pizza() {

    }

    public double getPizza_size() {
        return pizza_size;
    }

    public void setPizza_size(double pizza_size) {
        this.pizza_size = pizza_size;
    }

    public double getPizza_pepperoni() {
        return pizza_pepperoni;
    }

    public void setPizza_pepperoni(double pizza_pepperoni) {
        this.pizza_pepperoni = pizza_pepperoni;
    }

    public double getPizza_cheese() {
        return pizza_cheese;
    }

    public void setPizza_cheese(double pizza_cheese) {
        this.pizza_cheese = pizza_cheese;
    }

    public double getPizza_pineapple() {
        return pizza_pineapple;
    }

    public void setPizza_pineapple(double pizza_pineapple) {
        this.pizza_pineapple = pizza_pineapple;
    }
}
